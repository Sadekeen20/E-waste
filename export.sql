--  DDL for Trigger INVENTORY_ASK
--------------------------------------------------------

  CREATE OR REPLACE EDITIONABLE TRIGGER "C##RIDAY"."INVENTORY_ASK" 
AFTER INSERT
ON INVENTORY
FOR EACH ROW
DECLARE
  employee varchar(20);
  cond varchar(100);
  prodtype varchar(100);
BEGIN
  cond := :NEW.PRODUCT_CONDITION;
  SELECT PRODUCT_TYPE INTO prodtype FROM INVENTORY JOIN TAKE USING(INVENTORY_ID) JOIN PRODUCT_INFO USING(MODEL_NO) JOIN BARCODE_TABLE
  USING(MODEL_NO) WHERE INVENTORY.BARCODE = BARCODE_TABLE.BARCODE;
    IF UPPER(cond) = 'BAD' THEN
      SELECT EMPLOYEE_ID INTO employee FROM DISSEMBLER JOIN EMPLOYEE USING(EMPLOYEE_ID) WHERE UPPER(EMPLOYEE.STATUS) = 'FREE'
