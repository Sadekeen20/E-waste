  CREATE OR REPLACE FORCE EDITIONABLE VIEW "C##RIDAY"."EARNED_REFURBISHER" ("PROCESSOR_ID", "PRICE") AS 
  SELECT PROCESSOR_ID,PRICE
FROM REFURBISHER JOIN MAKES USING(PROCESSOR_ID)
