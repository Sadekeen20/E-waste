--  DDL for View VEHICLES_BUSY
--------------------------------------------------------

  CREATE OR REPLACE FORCE EDITIONABLE VIEW "C##RIDAY"."VEHICLES_BUSY" ("NAMES", "SOURCES", "DESTINATION", "ORDERS", "INDIVIDUAL") AS 
  (SELECT E.NAME,T.SOURCE,T.DESTINATION,K.ORDER_ID,I.NAME FROM EMPLOYEE E JOIN TRANSPORT T ON(E.EMPLOYEE_ID = T.EMPLOYEE_ID) JOIN TRANSPORTATION H ON(H.EMPLOYEE_ID = T.EMPLOYEE_ID)
JOIN ORDER_PROVIDER K ON(H.ORDER_ID = K.ORDER_ID) JOIN INDIVIDUAL_PROVIDES G ON(G.ORDER_ID = H.ORDER_ID) JOIN INDIVIDUAL I ON(I.PROVIDER_ID = G.PROVIDER_ID)
WHERE E.STATUS = 'busy' );
