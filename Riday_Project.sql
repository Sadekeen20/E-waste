CREATE OR REPLACE VIEW EMPLOYEE_CHECK(NAME, AGE, SALARY, PHONE) AS 
(SELECT E.NAME,E.AGE,E.SALARY,E.PHONE_NUMBER FROM EMPLOYEE "S" JOIN EMPLOYEE "E" USING(EMPLOYEE_ID) WHERE E.ACCESSLEVEL > S.ACCESSLEVEL);


CREATE SEQUENCE EMPLOYEE_ID_SEQ
INCREMENT BY 10
START WITH 1
MAXVALUE 1000000
NOCACHE
NOCYCLE;


CREATE OR REPLACE TRIGGER EMPLOYEE_ON_INSERT
  BEFORE INSERT ON EMPLOYEE
  FOR EACH ROW
BEGIN
  SELECT EMPLOYEE_ID_SEQ.nextval
  INTO :new.EMPLOYEE_ID
  FROM dual;
END;

